class UserUid {
  final String uid;
  final String? email;
  final String fname;
  final String lname;
  final String birthdate;
  final String phonenumber;
  final String city;
  final String street;
  final String streetnumber;

  UserUid({
    required this.uid,
    required this.email,
    required this.fname,
    required this.lname,
    required this.birthdate,
    required this.phonenumber,
    required this.city,
    required this.street,
    required this.streetnumber,
  });
}
